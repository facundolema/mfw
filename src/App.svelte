  <h1>An Eco-friendly Motherfucking Website</h1>
  <p>
    Why shouldn't I add <em>more</em> libraries to my project? Why shouldn't I
    use <em>more</em> animations? Why shouldn't I make my website <em>more</em>
    beautiful?
  </p>
  <h2>An Environmentally Friendly Approach to Web Development</h2>
  <p>
    It's no secret that we create gigantic amounts of data daily. In 2022, for
    example, we created <a
      href="https://explodingtopics.com/blog/data-generated-per-day"
    >
      approximately 330 million terabytes of data every day <sup>&#x2197;</sup
      ></a
    >. That's right, <em>every day</em>. And that number is only going to
    increase.
  </p>
  <p>
    Additionally, the number of internet users
    <a href="https://ourworldindata.org/grapher/number-of-internet-users">
      has consistently risen in the last decades <sup>&#x2197;</sup></a
    >, and it'll keep rising in the years to come. But this is good, right? Why
    is that a problem, you say? While it's certainly fascinating that more and
    more people have access to the internet, one must consider one thing: data
    centers.
  </p>
  <p>
    Data centers are specialized buildings that house computer systems, such as
    the servers that serve the website you're reading right now.
    <a
      href="https://www.iea.org/reports/data-centres-and-data-transmission-networks"
    >
      These data centers are responsible for 1% to 2% of the world's energy
      consumption
      <sup>&#x2197;</sup>
    </a>
    . To put that into perspective, that's
    <a
      href="https://www.iea.org/reports/data-centres-and-data-transmission-networks"
    >
      about the same amount of energy consumption as the entire airline industry
      <sup>&#x2197;</sup>
    </a>.
  </p>
  <p>
    Data centers also produce a lot of heat, which needs to be cooled down. This
    is usually done by air conditioning, which also consumes lot of energy. In
    fact,
    <a
      href="https://www.iea.org/reports/data-centres-and-data-transmission-networks"
    >
      the cooling systems of data centers consume about 40% of the total energy
      consumption of data centers <sup>&#x2197;</sup>
    </a>.
  </p>
  <p>
    However, it is true that energy consumption has risen at a slower rate than
    the number of internet users. This is because data centers have become more
    efficient over the years. But that doesn't mean we shouldn't do our part to
    reduce the amount of data we create and the energy we consume.
  </p>
  <p>
    Granted, not all of that data is created by web developers. In fact, most of
    it is created by the average person. But how can we, as web developers, do
    our part to reduce the amount of energy our websites consume?
  </p>
  <h2>
    I don't care about the environment. Why should I still make a website that
    looks like trash?
  </h2>
  <p>
    First of all, ouch. Second of all, a Motherfucking website doesn't need to
    be ugly, just try to KISS as much as possible while creating something
    visually appealing to your standards. There are many benefits to this
    approach, most of which are already listed <a
      href="https://motherfuckingwebsite.com/">here</a
    >
    (looks horrible? try
    <a href="http://bettermotherfuckingwebsite.com/">this one</a>,
    <a href="https://thebestmotherfucking.website/">this one</a> or
    <a href="https://perfectmotherfuckingwebsite.com/">this one</a>), and
    <a href="https://en.wikipedia.org/wiki/KISS_principle">here</a>. In short,
    by keeping your website simple and straightforward, you keep it lightweight,
    fast, accessible, less prone to bugs and it takes you less to create and
    maintain.
  </p>
  <h2>
    How to Reduce the Energy Consumption of Your Website (or How to KISS for
    environment foes)
  </h2>
  <ul>
    <li>
      <em>Use less libraries.</em> Libraries are great, but they also increase the
      size of your website. Avoid them if you can. If you can't, try to use smaller
      libraries.
    </li>
    <li>
      <em>Use less animations.</em> Animations are great, but they also increase
      the size of your website. Avoid them if you can. If you can't, try to use smaller
      animations.
    </li>
    <li>
      <em>Don't use frameworks you don't need.</em> Keeping it vanilla isn't
      always a viable (or recommendable) option, but before you jump into your
      typical
      <code>npx&nbsp;create-next-app@latest</code>, THINK! Do you really need a
      framework? If you do, do you really need that specific framework? Give
      <a href="https://svelte.dev/"> Svelte</a> a try. It's lightweight, fast, and
      it's the best thing since sliced bread.
    </li>
    <li>
      <em>Use less images.</em> Images are great, but they also increase the size
      of your website. Avoid them if you can. If you can't, try to use smaller images.
      No one needs a 4k image for a 100x100px thumbnail!.
    </li>
    <li>
      <em>Don't overengineer.</em> Save yourself time and energy by not creating
      unnecessary features. If you don't need it, don't create it. If you need it,
      create it in the simplest way possible.
    </li>
  </ul>

  <p>
    All of these are also true for mobile apps. A better app isn't the one with
    the most animations, the most libraries, or even the most features, but the
    one that does what it needs to do in the most efficient way possible.
  </p>
  <p>
    Strive to make your website or app as efficient as possible. Make it
    lightweight, make it fast. Save the planet. Save yourself time and your
    users computing power and battery.
  </p>
